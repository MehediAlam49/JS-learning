// Sum of two number
function sumOfTwoNumber(num1, num2) {
    let sum = num1 + num2;
    return sum;
}
console.log(sumOfTwoNumber(5, 6));

// Subtraction of two number
function subtractionOfTwoNumber(num1, num2) {
    let subtraction = num1 - num2;
    return subtraction;
}
console.log(subtractionOfTwoNumber(6, 5));